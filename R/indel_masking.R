# indel threshold and masking for Vie2010 filtered
Vie2010 <- c(2,11731118,4,8392714,5,6817475, 7, 6015118, 10,5288995,12,4955184,15,4578548,18,4283958,20,4118689)
Vie2010 <- matrix(Vie2010,ncol=2,byrow=TRUE)
#get differences
Vie2010_diffs <- cbind(rowMeans(cbind(Vie2010[-9,1],Vie2010[-1,1])),abs(diff(Vie2010)[,2]/diff(Vie2010)[,1]) )
pdf("vie2010_indelmask.pdf")
par(mar=c(5,4,4,5)+.1)
plot(Vie2010,xlab="indel threshold",ylab="# of masked bp",main="InDel masking for Vie2010",xlim=range(Vie2010[,1]))
par(new=T)
plot(Vie2010_diffs,axes=F,pch=4,col="red",xlab="",ylab="",xlim=range(Vie2010[,1]))
axis(4)
mtext("change of masked bps per minimal count change", side = 4,line = 3)
legend("topright",col=c("black","red"),pch=c(0,4),legend=c("bp masked","change of masked bp"))
dev.off()
